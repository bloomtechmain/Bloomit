(process as any).exit = () => { /* suppressed during init */ };

async function runSqlFile(filePath: string): Promise<void> {
  console.log(`\nрџ“„ Executing SQL file: ${path.basename(filePath)}`);
  const sql = fs.readFileSync(filePath, 'utf8');
  
  // Split by semicolon and execute statements
  const statements = sql.split(';')
    .map(s => s.trim())
    .filter(s => s.length > 0);

  let successCount = 0;
  let skipCount = 0;

  for (const statement of statements) {
    try {
      await pool.query(statement);
      successCount++;
    } catch (err: any) {
      // Ignore "relation already exists" errors
      if (err.code === '42P07') {
        skipCount++;
      } else if (err.code === '23505') {
        // Ignore unique constraint violations (duplicate data)
        skipCount++;
      } else {
        console.error(`   вќЊ Error in statement: ${statement.substring(0, 100)}...`);
        console.error(`   Error: ${err.message}`);
      }
    }
  }
  
  console.log(`   вњ… Executed ${successCount} statements, skipped ${skipCount}`);
}

async function runScript(scriptName: string): Promise<void> {
  const scriptPath = path.join(__dirname, scriptName);
  
  if (!fs.existsSync(scriptPath)) {
    console.log(`   вљ пёЏ  Script not found: ${scriptName} - Skipping`);
    return;
  }

  console.log(`\nрџ”§ Running script: ${scriptName}`);
  
  try {
    // Dynamic import for TypeScript modules
    const module = await import(scriptPath);
    
    // Try to find and execute the main function
    if (typeof module.default === 'function') {
      await module.default();
    } else if (typeof module.main === 'function') {
      await module.main();
    } else {
      // Some scripts auto-execute, so we've already imported them
      console.log(`   вњ… Script executed via import`);
    }
  } catch (error: any) {
    // Some errors are expected (table already exists, etc.)
    if (error.code === '42P07' || error.message?.includes('already exists')) {
      console.log(`   вЏ­пёЏ  Skipped - already exists`);
    } else {
      console.error(`   вљ пёЏ  Warning: ${error.message}`);
    }
  }
}

